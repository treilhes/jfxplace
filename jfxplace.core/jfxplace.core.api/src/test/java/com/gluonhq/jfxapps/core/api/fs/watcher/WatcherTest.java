package com.gluonhq.jfxapps.core.api.fs.watcher;

import static org.junit.jupiter.api.Assertions.assertDoesNotThrow;
import static org.junit.jupiter.api.Assertions.assertNotNull;
import static org.junit.jupiter.api.Assertions.assertThrows;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;

import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

@ExtendWith({MockitoExtension.class})
class WatcherTest {
    private Watcher watcher;
    private Path tempDir;

    @Mock
    WatchEventHandler handler;

    @BeforeEach
    void setUp() throws IOException {
        watcher = new Watcher();
        tempDir = Files.createTempDirectory("watcherTest");
    }

    @AfterEach
    void tearDown() throws IOException {
        watcher.stopWatch();
        Files.deleteIfExists(tempDir);
    }

    @Test
    void testConstructorInitializesWatchService() {
        assertNotNull(watcher);
    }

    @Test
    void testStartAndStopWatch() {
        watcher.startWatch();
        watcher.stopWatch();
        // No exception means success
    }

    @Test
    void testRegisterEventHandlerThrowsOnNull() {
        assertThrows(IllegalArgumentException.class, () -> {
            watcher.registerEventHandler(tempDir, null);
        });
    }

    @Test
    void testRegisterEventHandlerSuccess() {
        assertDoesNotThrow(() -> {
            watcher.registerEventHandler(tempDir, handler);
        });
    }
}
